public class pruebaJunit {

    public int getSuma(int a, int b) {
        return a + b;
    }

    public int getResta(int a, int b) {
        return a - b;
    }

    public int getInc(int a) {
        return a + 1;
    }

    public int getDec(int a) {
        return a - 1;
    }
}
